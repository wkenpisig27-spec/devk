#include <cstdio>
int main() {
    printf("sizeof(long) = %zu\n", sizeof(long));
    printf("sizeof(int) = %zu\n", sizeof(int));
    printf("sizeof(unsigned long) = %zu\n", sizeof(unsigned long));
    printf("sizeof(short) = %zu\n", sizeof(short));
    printf("sizeof(bool) = %zu\n", sizeof(bool));
    return 0;
}
